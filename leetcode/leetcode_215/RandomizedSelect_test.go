func compareTowString(s1 string, s2 string) int {
	if len(s1) > len(s2) {
		return 1
	} else if len(s1) < len(s2) {
		return -1
	} else {
		if s1 == s2 {
			return 0
		} else if s1 > s2 {
			return 1
		} else {
			return -1
		}
	}
}
